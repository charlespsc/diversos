# Podemos representar números muito grandes usando a representação de ponto flutuante. 
# Essa representação tem um grande ganho em eficiência, mas causa erros de arredondamento.
# Mesmo assim, se você programa com a linguagem Python, verá que você pode representar números gigantescos,
# como dois elevado a mil, sem erros de arredondamento. 

p = 2 ** 1000

print(p)